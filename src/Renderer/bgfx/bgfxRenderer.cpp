void* SDLNativeWindowHandle(SDL_Window* window) {
	SDL_SysWMinfo wmi;
	SDL_VERSION(&wmi.version);
	if (SDL_GetWindowWMInfo(window, &wmi) == false) {
		return nullptr;
	}
	return wmi.info.win.window;
}

void bgfxRenderer::init(const RendererConfig& config) {
	m_window = config.window;
	m_sdl_window = static_cast<SDL_Window*>(m_window->getNativeWindow());
	bgfx::renderFrame();

	bgfx::Init init;

	init.platformData.nwh = SDLNativeWindowHandle(m_sdl_window);
	init.resolution.width = m_window->getWidth();
	init.resolution.height = m_window->getHeight();
	init.resolution.reset = BGFX_RESET_VSYNC;
	if (bgfx::init(init) == false) {
		DRAUG_CORE_ERROR("Failed to init bgfx");
		return;
	}

	// Set view 0 to the same dimensions as the window and to clear the color buffer.
	m_view_id = 0;
	bgfx::setViewClear(m_view_id, BGFX_CLEAR_COLOR | BGFX_CLEAR_DEPTH, 0x303030ff, 1.0f, 0);
	//bgfx::setViewClear(m_view_id, BGFX_CLEAR_COLOR);
	bgfx::setViewRect(m_view_id, 0, 0, (uint16)m_window->getWidth(), (uint16)m_window->getHeight());
}

